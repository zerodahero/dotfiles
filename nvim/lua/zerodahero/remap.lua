vim.keymap.set("n", "<C-H>", "<C-W><C-H>")
vim.keymap.set("n", "<C-L>", "<C-W><C-L>")
vim.keymap.set("n", "<C-W>Z", "<C-W>| <C-W>_")
vim.keymap.set("n", "<C-W>z", "<C-W>=")

-- Tree
